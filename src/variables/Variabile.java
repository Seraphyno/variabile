package variables;

public class Variabile {
  
  public static void main(String[] args) {
    //declarare de constanta cu cuvant cheie "final"
    final double PI = 3.14;
    
    //declarare variabile separat de initializare
    int a;
    boolean valoareAdevar;
    char caracter;
    
    a = 1;
    valoareAdevar = true;
    caracter = 'A';
    
    
    //declarare variabile si initializare
    short s = -1;
    float alimentareCont = 1_000_000F;
    double plata = -500_000;
    String nume = "Daniel";
  }
}
